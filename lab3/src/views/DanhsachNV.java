        public NhanVien getFormData() {
        String maNhanVien = txtMa.getText();
        String tenNhanVien= txtTen.getText();
        String DiaChiNhanVien = txtDia.getText();
        int Tuoi = Integer.parseInt(txtTuoi.getText());
        NhanVien nv = new NhanVien(maNhanVien, tenNhanVien, Tuoi, DiaChiNhanVien);
        return nv;
    }
        public void loadTable() {
        ArrayList<NhanVien> ds = sinhVienRepo.search();
        ds = sinhVienRepo.search();
        DefaultTableModel tblModel = (DefaultTableModel) tblNhanVien.getModel();
        for(NhanVien nv: ds) {
            Object[] thongTinSV = {nv.getMaNhanVien(), nv.getTen(),nv.getTuoi(), nv.getDiaChi()};
            tblModel.addRow(thongTinSV);
        }
    }
   

