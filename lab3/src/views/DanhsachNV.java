    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        int index = tblNhanVien.getSelectedRow();
        String MaNhanVien = (String) tblNhanVien.getValueAt(index, 0);
        String Ten = (String) tblNhanVien.getValueAt(index, 1);
        Integer tuoi = (Integer) tblNhanVien.getValueAt(index, 2);
        String DiaChi = (String) tblNhanVien.getValueAt(index, 3);
        
        txtMa.setText(MaNhanVien);
        txtTen.setText(Ten);
        txtTuoi.setText(String.valueOf(tuoi));
        txtDia.setText(DiaChi);
    }//GEN-LAST:event_tblNhanVienMouseClicked

