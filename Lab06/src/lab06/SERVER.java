    @Override
    public void run() {
        try {
            input = new DataInputStream(socket.getInputStream());
            while (true) {
                if (socket != null) {
                    model.addElement("Slient say : " + input.readUTF());
                    lstho.setModel(model);
                } Thread.sleep(1000);
            }
            
        } catch (Exception e) {
        }
    }
