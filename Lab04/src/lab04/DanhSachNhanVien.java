public NhanVien getFormData() {
    String MaNhanVien = txtMaNhanVien.getText();
    String Ten = txtTen.getText();
   boolean GioiTinh = rdoNam.isSelected();
  
    String ChucVu = (String) cbxChucVu.getSelectedItem();
    NhanVien nv = new NhanVien(MaNhanVien, Ten, GioiTinh, ChucVu); 
    return nv;
}


    public void loadTable( ArrayList<NhanVien> ds) {
//        ArrayList<NhanVien> ds = NhanVienRepo.search();
//        ds = NhanVienRepo.search();
        DefaultTableModel tblModel = (DefaultTableModel) tblDanhSach.getModel();
        tblModel.setRowCount(0);
        for (NhanVien nv : ds) {
            Object[] thongTinSV = {nv.getMaNhanVien(), nv.getTen(), nv.isGioiTinh(), nv.getChucVu()};
            tblModel.addRow(thongTinSV);
        }
    }

