    private void tblDanhSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDanhSachMouseClicked
            int index = tblDanhSach.getSelectedRow();
    String MaNhanVien = (String) tblDanhSach.getValueAt(index, 0);
    String Ten = (String) tblDanhSach.getValueAt(index, 1);
    Boolean GioiTinh = (Boolean) tblDanhSach.getValueAt(index, 2);
    String ChucVu = (String) tblDanhSach.getValueAt(index, 3);

    txtMaNhanVien.setText(MaNhanVien);
    txtTen.setText(Ten);
        if (GioiTinh){
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
  
    
    cbxChucVu.setSelectedItem(ChucVu);
    }//GEN-LAST:event_tblDanhSachMouseClicked

